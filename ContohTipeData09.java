public class ContohTipeData09 {
    public static void main(String[] args) {
        
        char golonganDarah = 'A';
        byte jarak = (byte) 128;
        short jumlahPendudukDalamSatuan = 1250;
        float suhu = 60.50F;
        double berat = 0.21;
        long saldo = 271000000;
        int angkaDesimal = 0x30;

        System.out.println("Golongan darah  : " + (byte) golonganDarah);
        System.out.println("Jarak           : " + jarak);
        System.out.println("Jumlah Penduduk : " + jumlahPendudukDalamSatuan);
        System.out.println("Suhu            : " + suhu);
        System.out.println("Berat           : " + berat);
        System.out.println("Saldo           : " + saldo);
        System.out.println("Angka desimal   :" + angkaDesimal);

        
    }
}
